
public class MethodsMath {

	public static double maxValue(double numOne, double numTwo, double numThree) {
		Double output = 0.0;

		if (numOne > output) {
			output = numOne;
		} else {
			output = output;
		}

		if (numTwo > output) {
			output = numTwo;
		} else {
			output = output;
		}

		if (numThree > output) {
			output = numThree;
		} else {
			output = output;
		}

		return output;
	}

}
